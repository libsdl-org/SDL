// FIXME:
// Issues with the shader parser:
//
// 1: Structs confuse the parser.
// For example, take this autogenerated WGSL from Tint:
//
// struct S_1 {
//  tint_symbol_1 : array<SpriteVertex>,
// }
// @group(1u) @binding(0u) var<storage, read_write> VertexBuffer : S_1;
//
// This is valid WGSL. It will compile fine and won't complain while doing it.
// It'll unwrap the "S_1" into "array<SpriteVertex>" by itself.
//
// However, since the parser is quite crude, it only checks for "array<>", which, of course; it doesn't find.
// So, the question is, do we either want to:
// 1: Make some valid WGSL code just not work in SDLGPU.
// 2: Further complicate the parser.
//
// I'm leaning towards option one, mostly because being able to bind arrays into arbitrary structs is a stupid idea.

